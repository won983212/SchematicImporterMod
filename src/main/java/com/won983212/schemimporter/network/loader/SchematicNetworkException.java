package com.won983212.schemimporter.network.loader;

public class SchematicNetworkException extends RuntimeException {
    public SchematicNetworkException() {
        super();
    }

    public SchematicNetworkException(String s) {
        super(s);
    }

    public SchematicNetworkException(String message, Throwable cause) {
        super(message, cause);
    }

    public SchematicNetworkException(Throwable cause) {
        super(cause);
    }
}
